package fruit;

public class Product {
	
	private int type;
	private int number;
	private int price;
	
	public Product() {
	}
	
	public int getType() {
		return this.type;
	}
	
	public int setType(int type) {
		this.type=type;
		return this.type;
	}
	
	public int getNumber() {
		return this.number;
	}
	
	public int setNumber(int number) {
		this.number=number;
		return this.number;
	}
	
	public int getPrice() {
		return this.price;
	}
	public int setPrice(int price) {
		this.price=price;
		return this.price;
	}
	
}
